static void _print_bytes(uint8_t *bytes, size_t size)
{
    for (size_t i = 0; i < size; i++) {
        printf("%02X", bytes[i]);
    }

    puts("");
}

int main(void)
{
    int res;

    printf("Decoding %d bytes...\n", sizeof(encoded));
    _print_bytes(encoded, sizeof(encoded));

    res = wmbus_decoder_tmode(encoded, decoded, sizeof(result));

    for (size_t i = 0; i < sizeof(result); i++) {
        if (decoded[i] != result[i]) {
            _print_bytes(decoded, sizeof(decoded));
            _print_bytes(result, sizeof(result));
            printf("[ERROR] Code: %d\n", res);
            puts("[FAILURE]");
            return -1;
        }
    }

    puts("Decoded bytes:");
    _print_bytes(decoded, sizeof(decoded));
    puts("[SUCCESS]");
}
